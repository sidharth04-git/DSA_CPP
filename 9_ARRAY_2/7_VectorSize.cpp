// In Vector We Use v.size() to know the Size of Vector
#include<iostream>
#include<vector>
using namespace std;
int main()
{
    vector<int>v;
    // Inserting or Taking Input using push_back Operation
    v.push_back(4);       
    cout<<v.size()<<endl;
    v.push_back(7);
    cout<<v.size()<<endl;
    v.push_back(8);
    cout<<v.size()<<endl;
    v.push_back(1);
    cout<<v.size()<<endl;

// Sirf Element dekhta hai kitne hai aur Size Rakh deta hai accordingly
    
}